using namespace io;

int Multitest = 0;

void init() {}

void solve() {
    int n;
    rd(n);

    vector<int> a(n + 1);
    rd_vec(a, 1);

    int ans = 0;
    vector<int> diff(n + 1);
    for (int i = 2; i <= n; i += 2) {
        int pre = a[i - 1];
        int cur = a[i];


        if (cur > pre) {
            ans *= 2;
            ans += pre;
            int have = cur - pre;

            for (int j = i - 2; j >= 1; j -= 2) {
                if (diff[j] > 0) {
                    continue;
                }

                if (have + diff[j] > 0) {
                    ans += -diff[j];
                    have += diff[j];
                } else {
                    ans += have;
                    break;
                }
            }
        } else if (cur == pre) {
            ans *= 2;
            ans += cur;
        } else {
            ans += pre;
        }
        diff[i] = diff[i - 2] + cur - pre;
    }

    prt(ans);
}

signed main() {
    ios::sync_with_stdio(false);
    cin.tie(nullptr);
    init();
    int T = 1;
    if (Multitest) {
        rd(T);
    }
    while (T--)
        solve();
    return 0;
}
