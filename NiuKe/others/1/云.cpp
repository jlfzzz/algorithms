template<typename T>
class FenwickTree {
    vector<T> tree;

public:
    FenwickTree(int n) : tree(n + 1) {}

    void update(int i, T val) {
        for (; i < (int) tree.size(); i += i & -i) {
            tree[i] += val;
        }
    }

    // 左闭右闭
    T rangeSum(int l, int r) const { return this->pre(r) - this->pre(l - 1); }

    T pre(int i) const {
        T res = 0;
        for (; i > 0; i &= i - 1) {
            res += tree[i];
        }
        return res;
    }

    T getVal(int i) { return rangeSum(i, i); }

    void setVal(int i, T val) {
        T delta = val - getVal(i);
        update(i, delta);
    }

    // 点更新取 max
    void updateMax(int i, T val) {
        for (; i < (int) tree.size(); i += i & -i) {
            if (val > tree[i]) {
                tree[i] = val;
            }
        }
    }

    T preMax(int i) const {
        T res = numeric_limits<T>::min();
        for (; i > 0; i &= i - 1) {
            res = max(res, tree[i]);
        }
        return res;
    }
};


constexpr int N = 1e6 + 5;

int Multitest = 0;

void init() {}

void solve() {
    int n, m;
    rd(n, m);

    struct Seg {
        ll L, R;
        int Lid;
        int Rid;
    };

    vector<Seg> A(n), B(m);
    vector<ll> all;
    for (int i = 0; i < n; i++) {
        ll X, Y, P, Q;
        rd(X, Y, P, Q);

        ll L = Q - P;
        ll R = Y - X;

        A[i].L = L;
        A[i].R = R;
        all.pb(L);
        all.pb(R);
    }

    for (int i = 0; i < m; i++) {
        ll X, Y, P, Q;
        rd(X, Y, P, Q);

        ll L = Q - P;
        ll R = Y - X;

        B[i].L = L;
        B[i].R = R;
        all.pb(L);
        all.pb(R);
    }

    ranges::sort(all);
    all.erase(unique(all.begin(), all.end()), all.end());

    auto getId = [&](ll x) -> int {
        return int(ranges::lower_bound(all, x) - all.begin()) + 1;
    };

    int K = (int) all.size();
    for (int i = 0; i < n; i++) {
        A[i].Lid = getId(A[i].L);
        A[i].Rid = getId(A[i].R);
    }
    for (int i = 0; i < m; i++) {
        B[i].Lid = getId(B[i].L);
        B[i].Rid = getId(B[i].R);
    }
    vector<int> ordA(n);
    iota(ordA.begin(), ordA.end(), 0);
    sort(ordA.begin(), ordA.end(), [&](int i, int j) { return A[i].R < A[j].R; });
    vector<int> ordB(m);
    iota(ordB.begin(), ordB.end(), 0);
    sort(ordB.begin(), ordB.end(), [&](int i, int j) { return B[i].L < B[j].L; });

    FenwickTree<ll> bit(K + 2);
    ll ans = 0;
    int p = 0;

    for (int idxA: ordA) {
        ll RA = A[idxA].R;
        while (p < m && B[ordB[p]].L <= RA) {
            int idxB = ordB[p];
            bit.update(B[idxB].Rid, 1);
            p++;
        }

        int Lid = A[idxA].Lid;
        ll totalB = bit.pre(K);
        ll bad = bit.pre(Lid - 1);
        ans += (totalB - bad);
    }

    prt(ans);
}


int main() {
    ios::sync_with_stdio(false);
    cin.tie(nullptr);
    init();
    int T = 1;
    if (Multitest) {
        rd(T);
    }
    while (T--) {
        solve();
    }
}
