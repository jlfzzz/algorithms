void init() {}

void solve() {
    int x, c;
    cin >> x >> c;

    for (int i = x; i >= 0; i--) {
        if (i % 1000) {
            continue;
        }
        int t = i / 1000 * c;
        if (t + i <= x) {
            cout << i << '\n';
            return;
        }
    }
}

