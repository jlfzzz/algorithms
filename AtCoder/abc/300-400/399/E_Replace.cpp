queue<int> q;
set<char> st;
void topu() {
    for (char i = 'a'; i <= 'z'; i += 1)
        if (!r[i])
            q.push(i);
    while (!q.empty()) {
        int x = q.front();
        vis[x] = 1;
        q.pop();
        if (!vis[to[x]])
            r[to[x]] = 0, q.push(to[x]);
    }
}
