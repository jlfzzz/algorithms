int Multitest = 0;

void init() {}

void solve() {
    int N;
    rd(N);
    vl H(N);
    rv(H);

    vector<pair<ll, int>> st;

    ll current_sum = 0;

    vl ans;
    ans.reserve(N);

    for (ll val: H) {
        int count = 1;

        while (!st.empty() && st.back().fi < val) {
            current_sum -= st.back().fi * st.back().se;
            count += st.back().se;
            st.pop_back();
        }

        st.pb(val, count);
        current_sum += val * count;

        ans.pb(current_sum + 1);
    }

    prv(ans);
}

int main() {
    ios::sync_with_stdio(false);
    cin.tie(nullptr);
    init();
    int T = 1;
    if (Multitest) {
        rd(T);
    }
    while (T--) {
        solve();
    }
}
