constexpr int N = 1e6 + 5;

int Multitest = 1;

void init() {}

ll count_nodes(ll n, ll root, ll d) {
    if (d < 0)
        return 0;
    if (root > n)
        return 0;
    if (d == 0)
        return 1;
    if (d >= 62)
        return 0;

    i128 val = (i128) 1 << d;
    i128 start = (i128) root * val;

    if (start > n)
        return 0;

    i128 end = start + val - 1;
    i128 limit = n;

    if (end > limit)
        end = limit;

    return (ll) (end - start + 1);
}

void solve() {
    ll n, x, k;
    rd(n, x, k);

    ll ans = 0;

    ans += count_nodes(n, x, k);

    ll curr = x;
    ll dist_up = 0;

    while (curr > 1) {
        dist_up++;
        ll prev = curr;
        curr /= 2;

        ll rem = k - dist_up;

        if (rem < 0)
            break;

        if (rem == 0) {
            ans++;
        } else {
            ans += count_nodes(n, curr, rem) - count_nodes(n, prev, rem - 1);
        }
    }

    prt(ans);
}

int main() {
    ios::sync_with_stdio(false);
    cin.tie(nullptr);
    init();
    int T = 1;
    if (Multitest) {
        rd(T);
    }
    while (T--) {
        solve();
    }
}
