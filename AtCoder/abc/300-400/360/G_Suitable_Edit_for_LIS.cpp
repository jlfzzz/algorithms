template<typename T = long long>
class BIT {
    vector<T> tree;

public:
    BIT(int n) : tree(n + 1) {}

    void update(int i, T val) {
        for (; i < (int) tree.size(); i += i & -i) {
            tree[i] += val;
        }
    }

    // 左闭右闭
    T rangeSum(int l, int r) const { return this->pre(r) - this->pre(l - 1); }

    T pre(int i) const {
        T res = 0;
        for (; i > 0; i &= i - 1) {
            res += tree[i];
        }
        return res;
    }

    T getVal(int i) { return rangeSum(i, i); }

    void setVal(int i, T val) {
        T delta = val - getVal(i);
        update(i, delta);
    }

    // 点更新取 max
    void updateMax(int i, T val) {
        for (; i < (int) tree.size(); i += i & -i) {
            if (val > tree[i]) {
                tree[i] = val;
            }
        }
    }

    T preMax(int i) const {
        T res = numeric_limits<T>::min();
        for (; i > 0; i &= i - 1) {
            res = max(res, tree[i]);
        }
        return res;
    }
};

void init() {}

void solve() {
    int N;
    rd(N);
    vector<int> A(N);
    rv(A);

    vector<int> vals = A;
    sort(all(vals));
    vals.erase(unique(all(vals)), vals.end());

    auto get_rank = [&](int val) -> int { return lower_bound(all(vals), val) - vals.begin() + 1; };

    auto get_limit_rank = [&](int val) -> int { return upper_bound(all(vals), val) - vals.begin(); };

    int m = SZ(vals);
    BIT<ll> bit0(m), bit1(m), bit_lag(m);

    ll ans = 0;
    ll mx0 = 0;

    int prev_rk = -1;
    ll prev_len0 = -1;

    for (int i = 0; i < N; i++) {
        int x = A[i];
        int rk = get_rank(x);

        ll q0 = bit0.preMax(rk - 1);
        ll len0 = (q0 < 0 ? 0 : q0) + 1;

        ll q1 = bit1.preMax(rk - 1);
        ll cand1 = (q1 < 0 ? 0 : q1) + 1;

        ll cand2 = 0;
        if (i > 0) {
            cand2 = 2;

            if (i > 1) {
                int limit_rk = get_limit_rank(x - 2);
                if (limit_rk > 0) {
                    ll q_lag = bit_lag.preMax(limit_rk);
                    if (q_lag >= 0) {
                        cand2 = max(cand2, q_lag + 2);
                    }
                }
            }
        }

        ll len1 = max(cand1, cand2);

        ll cand3 = mx0 + 1;

        ans = max({ans, len1, cand3});

        bit0.updateMax(rk, len0);
        bit1.updateMax(rk, len1);

        if (i > 0) {
            bit_lag.updateMax(prev_rk, prev_len0);
        }

        prev_rk = rk;
        prev_len0 = len0;
        mx0 = max(mx0, len0);
    }

    prt(ans);
}

int main() {
    ios::sync_with_stdio(false);
    cin.tie(nullptr);
    init();
    int T = 1;
    if (Multitest) {
        rd(T);
    }
    while (T--) {
        solve();
    }
}
