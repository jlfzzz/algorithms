// ---------------------------------------------
// 曼哈顿距离 ↔ 切比雪夫距离 转换笔记
// ---------------------------------------------
//
// 原始定义：
//   曼哈顿距离 (Manhattan distance)
//     dM = |x1 - x2| + |y1 - y2|
//
//   切比雪夫距离 (Chebyshev distance)
//     dC = max(|X1 - X2|, |Y1 - Y2|)
//
// 关键思想：坐标旋转 + 线性变换
//   将平面 (x, y) 旋转 45° 并缩放后，
//   曼哈顿距离在新坐标系下变为切比雪夫距离。
//
//
//
//
// *********************************
// 坐标变换公式：
//   X = x + y
//   Y = x - y
// *********************************
//
// 于是可以得到：
//   dM = |x1 - x2| + |y1 - y2|
//      = max( |(x1 + y1) - (x2 + y2)|,
//              |(x1 - y1) - (x2 - y2)| )
//
// 换句话说：
//   在 (X, Y) 坐标系下，
//   曼哈顿距离 = 切比雪夫距离。
//
// 常见用途：
//   1. 将“上下左右四方向”的曼哈顿距离问题
//      转为“八方向”的切比雪夫距离问题，方便 BFS 或 DP。
//   2. 求平面上点集的最大/最小曼哈顿距离时，
//      只需在 (x+y, x−y) 平面上求极值差。
// ---------------------------------------------
