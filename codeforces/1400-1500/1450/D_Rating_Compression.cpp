using namespace io;

int Multitest = 1;

void init() {}

int n;
int a[300010], cnt[300010];

void solve() {
    cin >> n;
    for (int i = 1; i <= n; i++)
        cnt[i] = 0;
    bool f = 1;
    for (int i = 1; i <= n; i++) {
        cin >> a[i];
        if (cnt[a[i]])
            f = 0;
        cnt[a[i]]++;
    }
    int d = 1, l = 1, r = n;
    cout << f;
    for (int i = n - 1; i >= 2; i--) {
        if (cnt[n - i] != 1 || (a[l] != n - i && a[r] != n - i) || !cnt[n - i + 1]) {
            d = i;
            break;
        }
        if (a[l] == n - i)
            l++;
        else
            r--;
    }
    for (int i = 2; i <= n - 1; i++)
        if (i > d)
            cout << 1;
        else
            cout << 0;
    if (n == 1) {
        cout << "\n";
        return;
    }
    if (cnt[1])
        cout << 1;
    else
        cout << 0;
    cout << "\n";
}

signed main() {
    ios::sync_with_stdio(false);
    cin.tie(nullptr);
    init();
    int T = 1;
    if (Multitest) {
        rd(T);
    }
    while (T--)
        solve();
    return 0;
}
