using namespace io;

int Multitest = 1;

void init() {}

void solve() {
    int n;
    rd(n);
    vector<int> a(n);
    rd_vec(a);
    vector<int> b(n);

    if (n % 2 == 0) {
        for (int i = 0; i < n; i += 2) {
            b[i] = a[i + 1];
            b[i + 1] = -a[i];
        }
    } else {
        int i = 0, j = 1, k = 2;
        if (a[i] + a[j] != 0) {
            b[i] = a[k];
            b[j] = a[k];
            b[k] = -(a[i] + a[j]);
        } else if (a[i] + a[k] != 0) {
            b[i] = a[j];
            b[k] = a[j];
            b[j] = -(a[i] + a[k]);
        } else {
            b[j] = a[i];
            b[k] = a[i];
            b[i] = -(a[j] + a[k]);
        }
        for (int t = 3; t < n; t += 2) {
            b[t] = a[t + 1];
            b[t + 1] = -a[t];
        }
    }

    prt_vec(b);
}

signed main() {
    ios::sync_with_stdio(false);
    cin.tie(nullptr);
    init();
    int T = 1;
    if (Multitest) {
        rd(T);
    }
    while (T--)
        solve();
    return 0;
}
