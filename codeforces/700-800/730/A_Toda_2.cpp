vector<int> reduce(int x) {
    vector<pi> bv;
    for (int j = 0; j < n; j++)
        bv.emplace_back(-b[j], j);
    sort(bv.begin(), bv.end());
    vector<int> v;
    for (int i = 0; i < bv.size() && i < x; i++) {
        if (b[bv[i].second])
            b[bv[i].second]--;
        v.push_back(bv[i].second);
    }
    return v;
}

