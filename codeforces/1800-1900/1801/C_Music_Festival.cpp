int Multitest = 1;

void init() {}

void solve() {
    int n;
    rd(n);

    vector<tuple<int, int, int>> events;
    vector<int> coords;

    for (int _: range(n)) {
        int k;
        rd(k);

        vector<int> v(k);
        rd_vec(v);

        int curMax = -1;
        vector<int> temp;
        for (int x: v) {
            if (x > curMax) {
                temp.pb(x);
                curMax = x;
            }
        }

        int sz = (int) temp.size();
        int albumMax = temp.back();
        int rPlus = albumMax + 1;
        for (int i: range(sz - 1, -1, -1)) {
            int cur = temp[i];
            int cnt = sz - i;
            events.eb(cur, rPlus, cnt);
            coords.pb(cur);
            coords.pb(rPlus);
        }
    }

    ranges::sort(coords);
    coords.erase(unique(coords.begin(), coords.end()), coords.end());

    auto getId = [&](int x) {
        int id = (int) (ranges::lower_bound(coords, x) - coords.begin()) + 1;
        return id;
    };

    int m = (int) coords.size();
    vector<vector<pair<int, int>>> grouped(m + 1);
    for (auto &[cur, rPlus, cnt]: events) {
        int i = getId(cur);
        int j = getId(rPlus);
        grouped[i].eb(j, cnt);
    }

    FenwickTree<long long> bit(m);
    bit.updateMax(1, 0LL);
    for (int i: range(1, m + 1)) {
        long long base = bit.preMax(i);
        for (auto &[to, cnt]: grouped[i]) {
            bit.updateMax(to, base + cnt);
        }
    }

    long long ans = bit.preMax(m);
    prt(ans);
}

signed main() {
    ios::sync_with_stdio(false);
    cin.tie(nullptr);
    init();
    int T = 1;
    if (Multitest) {
        rd(T);
    }
    while (T--)
        solve();
    return 0;
}
