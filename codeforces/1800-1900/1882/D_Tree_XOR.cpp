void init() {}

void solve() {
    int n;
    rd(n);

    vector<int> a(n + 1);
    rd_vec(a, 1);

    vector<vector<int>> g(n + 1);
    for (int i: range(n - 1)) {
        int u, v;
        rd(u, v);
        g[u].pb(v);
        g[v].pb(u);
    }

    vector<int> dp(n + 1), sz(n + 1);
    auto dfs1 = [&](this auto &&dfs, int u, int fa) -> void {
        sz[u] = 1;
        for (int v: g[u]) {
            if (v == fa) {
                continue;
            }

            dfs(v, u);
            sz[u] += sz[v];
            dp[u] += dp[v] + sz[v] * (a[u] ^ a[v]);
        }
    };

    dfs1(1, 0);

    vector<int> ans(n + 1);
    auto dfs2 = [&](this auto &&dfs, int u, int fa) -> void {
        ans[u] = dp[u];
        for (int v: g[u]) {
            if (v == fa) {
                continue;
            }

            // dp[v] -= sz[v] * (a[v] ^ a[u]);
            dp[v] += (n - sz[v]) * (a[v] ^ a[u]) + (dp[u] - dp[v] - sz[v] * (a[u] ^ a[v]));
            dfs(v, u);
        }
    };

    dfs2(1, 0);

    prt_vec(ans, 1);
}

