using namespace io;

int Multitest = 1;

void init() {}

void solve() {
    string s;
    rd(s);

    int n = s.size();
    int open = 0, cnt = 0;
    for (char c: s) {
        if (c == '(')
            open++;
        else if (c == '?')
            cnt++;
    }
    int need = n / 2 - open;
    vector<int> pos;
    for (int i = 0; i < n; i++)
        if (s[i] == '?')
            pos.push_back(i);

    string t = s;
    for (int i = 0; i < pos.size(); i++)
        t[pos[i]] = (i < need ? '(' : ')');

    if (need == 0 || need == cnt) {
        prt("YES");
        return;
    }


    string tt = t;
    tt[pos[need - 1]] = ')';
    tt[pos[need]] = '(';

    int left = 0;
    bool ok = true;
    for (int i = 0; i < n; i++) {
        left += (tt[i] == '(' ? 1 : -1);
        if (left < 0) {
            ok = false;
            break;
        }
    }
    if (ok)
        prt("NO");
    else
        prt("YES");
}

signed main() {
    ios::sync_with_stdio(false);
    cin.tie(nullptr);
    init();
    int T = 1;
    if (Multitest) {
        rd(T);
    }
    while (T--)
        solve();
    return 0;
}
