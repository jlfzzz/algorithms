using namespace io;

int Multitest = 0;

void init() {}

void solve() {
    int n;
    rd(n);
    vector<int> a(n);
    rd_vec(a);

    int mx = ranges::max(a);
    int m = bit_width((ull) mx);
    int u = 1 << m;

    vector<int> dp(u, -1);
    for (int x: a) {
        dp[x] = x;
    }

    for (int mask = 1; mask < u; mask++) {
        if (dp[mask] == -1) {
            continue;
        }


        for (int i = 0; i < m; i++) {
            if ((mask >> i & 1) == 0) {
                dp[mask | (1 << i)] = dp[mask];
            }
        }
    }

    vector<int> ans;
    int allOne = u - 1;
    for (int x: a) {
        if (dp[x ^ allOne] == -1) {
            ans.push_back(-1);
        } else {
            ans.push_back(dp[x ^ allOne]);
        }
    }

    prt_vec(ans);
}

signed main() {
    ios::sync_with_stdio(false);
    cin.tie(nullptr);
    init();
    int T = 1;
    if (Multitest) {
        rd(T);
    }
    while (T--)
        solve();
    return 0;
}
