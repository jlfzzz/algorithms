int Multitest = 1;

void init() {}

void solve() {
    int n;
    rd(n);

    vector<pii> a(n);
    map<int, int> val;
    for (int i: range(n)) {
        int t;
        rd(t);
        a[i] = {t, i};
        val[i] = t;
    }

    vector<int> p(n);
    rd_vec(p);

    ranges::sort(a, greater<>());
    int p1 = 0;

    int ans = 0;
    int choose = inf;

    multiset<int> st, st2;
    vector<int> del(n);

    for (int i: range(n)) {
        // debug("i", i);
        if (i - 1 >= 0) {
            int pos = p[i - 1];
            pos--;

            del[pos] = 1;
            if (st2.contains(pos)) {
                st2.erase(st2.find(pos));
                st.erase(st.find(val[pos]));
            }
        }

        while (p1 < n && st.size() < i + 1) {
            // debug("p1", p1);
            auto [v, id] = a[p1];
            if (del[id]) {
                p1++;
                continue;
            }

            st.insert(v);
            st2.insert(id);
            p1++;
        }
        if (st.size() < i + 1) {
            break;
        }

        int total = st.size() * (*st.begin());
        if (total > ans) {
            ans = total;
            choose = i + 1;
        }
    }

    prt(ans, choose);
}

signed main() {
    ios::sync_with_stdio(false);
    cin.tie(nullptr);
    init();
    int T = 1;
    if (Multitest) {
        rd(T);
    }
    while (T--)
        solve();
    return 0;
}
