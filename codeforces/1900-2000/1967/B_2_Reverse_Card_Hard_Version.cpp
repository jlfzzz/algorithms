void init() {}

void solve() {
    int n, m;
    cin >> n >> m;
    int ans = 0;
    for (int a = 1; a <= n / a; ++a) {
        for (int b = 1; b <= m / b; ++b) {
            if (gcd(a, b) == 1)
                ans += min(n / a, m / b) / (a + b);
        }
    }

    cout << ans << '\n';
}

signed main() {
    ios::sync_with_stdio(false);
    cin.tie(nullptr);
    init();
    int T = 1;
    cin >> T;
    while (T--)
        solve();
    return 0;
}
