int Multitest = 1;

void init() {}

void solve() {
    int n;
    rd(n);

    vector<pll> ans;
    vector<int> a(n);
    rd_vec(a);

    UnionFind uf(n);

    for (int i: range(n - 1, 0, -1)) {
        vector<int> temp(i, -1);
        for (int j: range(0, n)) {
            int rootj = uf.find(j);
            if (rootj != j)
                continue;
            int t = a[j] % i;
            if (temp[t] == -1) {
                temp[t] = j;
            } else {
                int other = temp[t];
                uf.unite(other, j);
                ans.eb(other, j);
                break;
            }
        }
    }

    prt("YES");
    for (int idx = ans.size() - 1; idx >= 0; --idx) {
        prt(ans[idx].first + 1, ans[idx].second + 1);
    }
}

