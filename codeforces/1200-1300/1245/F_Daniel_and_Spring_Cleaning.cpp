constexpr int N = 1e6 + 5;

int Multitest = 1;

void init() {}

ll dp[31][2][2];

void solve() {
    ll l, r;
    rd(l, r);

    auto calc = [&](ll x, ll y) -> ll {
        if (x < 0 || y < 0)
            return 0;

        memset(dp, -1, sizeof(dp));

        auto dfs = [&](this auto &&dfs, int i, int tx, int ty) -> ll {
            if (i < 0) {
                return 1;
            }

            ll &memo = dp[i][tx][ty];
            if (memo != -1)
                return memo;
            memo = 0;

            int bx = (x >> i) & 1;
            int by = (y >> i) & 1;

            for (int a: {0, 1}) {
                for (int b: {0, 1}) {
                    if (a & b)
                        continue;

                    if (tx && a > bx)
                        continue;
                    if (ty && b > by)
                        continue;

                    int ntx = tx && (a == bx);
                    int nty = ty && (b == by);

                    memo += dfs(i - 1, ntx, nty);
                }
            }
            return memo;
        };

        return dfs(30, 1, 1);
    };

    ll ans;
    if (l == 0) {
        ans = calc(r, r);
    } else {
        ans = calc(r, r) - 2 * calc(l - 1, r) + calc(l - 1, l - 1);
    }

    prt(ans);
}


int main() {
    ios::sync_with_stdio(false);
    cin.tie(nullptr);
    init();
    int T = 1;
    if (Multitest) {
        rd(T);
    }
    while (T--) {
        solve();
    }
}
