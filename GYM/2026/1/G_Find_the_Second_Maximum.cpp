using namespace utils;

constexpr int N = 1e6 + 5;

int Multitest = 1;

void init() {}

void solve() {
    int n;
    rd(n);

    auto ask = [&](int i, int j) {
        cout << "? " << i << " " << j << endl;
        int t;
        rd(t);

        return t;
    };

    int r = n + 1;
    int l = 1;
    int mx = ask(1, n);
    int pos = 1;
    while (l < r) {
        int mid = (l + r) / 2;
        int t = ask(1, mid);

        if (t < mx) {
            l = mid + 1;
        } else {
            pos = mid;
            r = mid;
        }
    }

    l = 1, r = n + 1;
    int mx2 = 0;
    if (pos == 1) {
        l = 2;
        mx2 = ask(2, n);
    } else if (pos == n) {
        r = n;
        mx2 = ask(1, n - 1);
    } else {
        int t1 = ask(1, pos - 1);
        int t2 = ask(pos + 1, n);

        if (t1 > t2) {
            mx2 = t1;
            r = pos;
        } else {
            mx2 = t2;
            l = pos + 1;
        }
    }

    int ans = 0;
    int tl = l;
    while (l < r) {
        int mid = (l + r) / 2;
        int t = ask(tl, mid);

        if (t < mx2) {
            l = mid + 1;
        } else {
            ans = mid;
            r = mid;
        }
    }

    cout << "! " << mx2 << " " << ans << endl;
}

int main() {
    ios::sync_with_stdio(false);
    cin.tie(nullptr);
    init();
    int T = 1;
    if (Multitest) {
        rd(T);
    }
    while (T--) {
        solve();
    }
}
