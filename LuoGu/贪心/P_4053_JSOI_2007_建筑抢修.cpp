using namespace io;

int Multitest = 0;

void init() {}

void solve() {
    int n;
    rd(n);

    vector<pii> arr(n);
    For(i, n) {
        int a, b;
        rd(a, b);
        arr[i] = {a, b};
    }

    ranges::sort(arr, [](pii &a, pii &b) { return a.second < b.second; });

    priority_queue<int> pq;
    int sum = 0;
    for (auto &[t1, t2]: arr) {
        if (t1 + sum <= t2) {
            pq.push(t1);
            sum += t1;
        } else {
            if (pq.empty()) {
                continue;
            }

            if (sum - pq.top() + t1 <= t2 && t1 < pq.top()) {
                sum -= pq.top();
                pq.pop();
                pq.push(t1);
                sum += t1;
            }
        }
    }

    int ans = pq.size();
    prt(ans);
}

signed main() {
    ios::sync_with_stdio(false);
    cin.tie(nullptr);
    init();
    int T = 1;
    if (Multitest) {
        rd(T);
    }
    while (T--)
        solve();
    return 0;
}
